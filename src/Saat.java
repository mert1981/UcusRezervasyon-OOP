
public class Saat {

	private int saat;
	private int dakika;
	private int saniye;
	
	public Saat(int saat, int dakika, int saniye) {
		
		this.saat = saat;
		this.dakika = dakika;
		this.saniye = saniye;
	}

	public int getSaat() {
		return saat;
	}

	public void setSaat(int saat) {
		this.saat = saat;
	}

	public int getDakika() {
		return dakika;
	}

	public void setDakika(int dakika) {
		this.dakika = dakika;
	}

	public int getSaniye() {
		return saniye;
	}

	public void setSaniye(int saniye) {
		this.saniye = saniye;
	}
	
	
	
}
