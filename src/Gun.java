
public class Gun {

	private int gun;
	private int ay;
	private int yil;
	
	public Gun(int gun, int ay, int yil) {
		super();
		this.gun = gun;
		this.ay = ay;
		this.yil = yil;
	}

	public int getGun() {
		return gun;
	}

	public void setGun(int gun) {
		this.gun = gun;
	}

	public int getAy() {
		return ay;
	}

	public void setAy(int ay) {
		this.ay = ay;
	}

	public int getYil() {
		return yil;
	}

	public void setYil(int yil) {
		this.yil = yil;
	}
	
	
}
